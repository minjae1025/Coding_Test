SELECT
    SUBSTR(SALES_DATE, 1) AS SALES_DATE, PRODUCT_ID, USER_ID, SALES_AMOUNT
FROM 
    (SELECT SALES_DATE, PRODUCT_ID, USER_ID, SALES_AMOUNT
     FROM ONLINE_SALE 
     UNION
     SELECT SALES_DATE, PRODUCT_ID, NULL, SALES_AMOUNT
     FROM OFFLINE_SALE  
    ) AS SALE
WHERE
    SALES_DATE BETWEEN '2022-03-01' AND '2022-03-31'
ORDER BY 
    SALES_DATE,
    PRODUCT_ID,
    USER_ID